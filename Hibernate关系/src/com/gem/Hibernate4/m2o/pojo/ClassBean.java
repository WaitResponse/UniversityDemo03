package com.gem.Hibernate4.m2o.pojo;

public class ClassBean {
	private String cid;
	private String cname;
	
	public ClassBean(){}

	public String getCid() {
		return cid;
	}

	public void setCid(String cid) {
		this.cid = cid;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	
	
}
